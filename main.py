# Основной цикл программы
while True:
    # Читаем сообщение пользователя
    message = input("Ваше сообщение: ")

    # Если пользователь говорит "привет"
    if message.lower() == "привет":
        print("Привет! Как я могу тебе помочь?")
    # Если пользователь говорит "пока"
    elif message.lower() == "пока":
        print("Пока! Буду ждать тебя снова.")
        break
    # Если пользователь говорит что-то другое
    else:
        print("Извини, я не понимаю. Попробуй сказать что-то другое.")
